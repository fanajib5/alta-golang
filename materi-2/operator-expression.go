package main

import "fmt"

func main() {
	// OPERAND DAN OPERATOR
	x := 1 + 2
	fmt.Println(x)

	a := 5
	b := 10
	c := a + b
	fmt.Println(c)

	// LUAS SEGITIGA
	alas := 10
	tinggi := 20
	luas := (alas * tinggi) / 2
	fmt.Println(luas)

	// STRING OPERATION
	helloWorld := "Hello" + " " + "World!"
	fmt.Println(helloWorld)
}
